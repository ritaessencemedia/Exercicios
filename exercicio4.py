# pede ao usuário imputar uma frase na varável "frase"
frase = input("Digite uma palavra ou frase: ")

# Calcula o número de caracteres da variável "frase" com a função len, e armazena na variável "num_caracteres"
num_caracteres = len(frase)

# Exibe o número de caracteres
print(f"O número de caracteres (incluindo espaços) é: {num_caracteres}")
